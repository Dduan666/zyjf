		    </dd>
		    <dt>银行卡没有注销会影响个人信誉吗？</dt>
		     <dd>

		      <p>银行卡这种“塑料硬币”大家都不陌生，然而小小的银行卡只要稍不注意就可能成为信用“地雷”。近日，网上一条微博被转发数次，该微博称杨先生5年前弃用的银行卡莫名其妙欠了5分钱，如今他不但要为此偿还银行几百元利息，还被列入央行个人征信系统不良信用顾客名单。你手中是否也有一张或多张不用的银行卡在家中“沉睡”，它们真的会影响到个人信用吗？ 答案是肯定的。目前银行卡一般分为借记卡和贷记卡两大类，借记卡就是我们常用的储蓄卡，没有透支功能，而贷记卡就是所谓的信用卡，具有透支功能。当储蓄卡被闲置后，各家银行会收取相应的年费，同时各家银行根据日均存款的条件也可能会收取“小额账户管理费”，以某大型国有银行为例，账户当季日均余额不足 300元，该账户就会被银行自动扣收每季度3元的管理费、10元年费。
		      </p>
		        <p>
		      	对于储蓄卡客户来说，如果账户里的资金不够扣除各种费用，甚至出现零金额或负金额时，就会被停止使用，只有再次交清欠费才能恢复使用。而且几年后这个账户就会自动注销，但不可能出现倒欠银行钱的情况，所以也不会影响到个人信用记录。但是对于信用卡客户来说，如果信用卡被启用后长期闲置，就会产生相应的年费，如果信用卡客户没有按时缴纳年费则会产生利息和滞纳金，从而造成透支。有的市民是在准备贷款买房时查询信用报告后，才记起自己名下还有平时不用的银行卡。这些“休眠卡”虽然长期没有使用，但可能依然会被银行收取年费或管理费，从而造成欠款未还，影响个人信用。 为此业内人士提醒持卡人，“睡眠卡”应及时销户。居民在办卡时，首先一定要弄清楚自己的银行卡属于何种卡，是储蓄卡还是信用卡，是否具有透支功能，怎样清算年费。同时，银行卡用户还应定期对自己的银行卡进行清理，对手中闲置不用的“睡眠”银行卡，最好及时到相应银行办理销户手续。办理销户手续时应携带个人身份证以及需要销户的银行卡。而如果市民确实因为没给手中闲置的信用卡交年费而产生不良记录，可以先向所属银行提出申辩，请银行出具非恶意透支证明，然后向人民银行提出异议，人民银行会对相关银行进行调查了解，如属实，可由人民银行总行进行修改。
		      </p>
		    </dd>
