		    <dt>借款人有资产抵押，为何不直接通过银行贷款呢?</dt>
		    <dd>

		      <p>这是因为银行小额贷款的营销成本较高，小企业向银行直接申请贷款受理较难，这就造成有融资的中小企业往往会向担保机构等融资机构求救，担保机构选择客户的成本比较低，从中选择优质项目推荐给合作银行，提高融资的成功率，就会降低银行小额贷款的营销成本。</p>

		      <p>另外，在贷款事中风险控制方面，银行不愿在小额贷款上投放，有一个重要的原因是银行此类贷款的管理成本较高，而收益并不明显，对于这类贷款，担保机构可以通过优化贷中管理流程，形成对于小额贷后管理的个性化服务，分担银行的管理成本，免去银行后顾之忧。</p>
		      <p>
		      	再次，事后风险释放，担保机构的优势更是无可替代的，银行直贷的项目出现风险，处置抵押物往往周期长，诉讼成本高，变现性不佳，担保机构的现金代偿，大大解决了银行处置难的问题，有些担保机构做到1个月（投资担保甚至3天）贷款逾期即代偿，银行的不良贷款及时得到消除，之后再由担保机构通过其相比银行更加灵活的处理手段进行风险化解。
		      </p>

		    </dd>

		    <dt>银行不当操作导致有缺陷担保存在?</dt>
		    <dd>

		      <p>主要表现在：一是担保合同中规定必须有担保方法定代表人或授权代理人签字并加盖公章，而一些合同中只有签字或只有盖章；二是在贷款到期未归还时，银行不注重对担保人的催收，催收通知单上往往只有借款人签字盖章，而无担保人的签章；三是有的担保合同期限短于贷款合同期限等。</p>
		    </dd>

		    <dt> 我能贷多少？</dt>
		    <dd>

		      <p>用户所能贷款的额度跟自身资质（资产和征信）、所选的渠道及所选的产品有关。不同的渠道对借贷者的资质判断不同，风险偏好也不同，都会影响到借贷者的贷款额度，这些必须根据您的实际情况做现场判断。您可以与我们的顾问联系，根据您的情况做个评估。</p>
		    </dd>

		    <dt> 什么是个人征信?</dt>
		    <dd>

		      <p>征信记录了个人过去的信用行为，这些行为体现在个人信用报告中，就是人们常说的“信用记录”。信用记录将影响个人未来的经济活动。

逾期还款、欠账、法院经济纠纷被起诉、被执行记录、信用卡违约、都会影响你的个人信用记录</p>
		    </dd>
		    <dt>  我的资质一般，怎么才能贷到更多的钱？</dt>
		    <dd>

		      <p>想要将自己资质对应的贷款额度最大化，除了找到相对优质的贷款渠道和产品以外，还可以通过组合贷款来满足需求，这就需要专业的融资顾问为你量身打造适合你的融资方案。</p>
		    </dd>
		    <dt> 征信差会怎么样？</dt>
		    <dd>

		      <p>信用如果有污点，会影响贷款额度，并且贷款成本也会提高。如果情节较重，你可能会无法在银行、小额贷款公司等金融机构申请贷款。具体规定每家银行各有不同。详细情况根据个人征信记录为准。</p>
		    </dd>
		    <dt> 抵押贷款必须要有房产吗</dt>
		    <dd>

		      <p>也可以是是其他物品，但是房产是常见的金额较大的抵押物，比较容易借贷 抵押贷款人按《担保法》规定的抵押方式以借款人或第三人的财产作为抵押物发放的贷款。可做抵押物的财产包括： 抵押人有权自主支配的房产和其他土地上定着物； 抵押人依法取得的国家土地使用权； 贷款人认可的其他财产。 贷款人与抵押人签订抵押合同后，双方必须依照有关法律规定办理抵押物登记。抵押合同自抵押物登记之日起生效，到借款人还清全部贷款本息时终止。抵押全设定后，所有能够证明抵押物权属的证明文件（原件）及抵押物的保险单证（正本）等，均由贷款人保管并承担保管责任。</p>
		    </dd>
		    <dt>  购房申请贷款应该注意以下事项？</dt>
		    <dd>

		      <p>  1、 查询确认自己的信用报告在贷款之前一定要对自己的信用状况进行查询，以此来判断自己是否有贷款的资格，可以通过征信管理部门查询本人的信用报告。很多情况都可能会造成信用状况差导致无法贷款，比如有逾期不归还信用卡欠款的信用记录等。所以说平时要注意自己的信用记录，以免因为小事耽误了贷款。</p>
		      <p>    2、 个人信息要准确在申请购房贷款时需要提交个人信息，个人信息应详细、准确；而在贷款发放后，也要注意的自己的个人信息保持准确，如果信息发生变动应及时告知银行。确保银行方面有自己的准确信息，方便银行联系贷款人，这样可以及时了解银行方面的相关信息，可以更好地享受银行为借款人提供的贷后服务；而如果没有及时把最新信息告知银行，则可能会带来很多麻烦，接收不到银行方面的相关通知。</p>
		      <p>  3、 借款合同和借据妥善保管,很多借款人在贷款发放下来之后就万事大吉了，借款合同和借据随便一丢，但是其实这种行为是存在风险的。我们应该了解借款合同和借据都是重要的法律文件，借款人与银行的相关交易详情都在这些文件中。另外，由于贷款期限长度不等，最高贷款期限长达30年，为防止中间出什么差错，作为借款人，应当妥善保管您的合同和借据，不要遗失借款合同和借据。
 </p>
 <p>
 4、 贷款还清后不要忘记撤销抵押 很多购房者还完贷款之后就以为万事大吉了，但其实撤销抵押是个很重要的环节。购房者再还清了全部贷款本金和利息后，可以拿银行的贷款结清证明和《房地产他项权利证明》前往房产所在地区的房地产交易中心办理抵押撤消手续。</p>
		    </dd>
